/******************************************************************************
Function:       get_config
Description:   get TP config file
Input:           moudle_id: 0->OFLIM , 2->ECW, 8->TRULY, 10->JUNDA; 
                    ic_number:2202
Output:         NA
Return:         Config file
Others:         NA
******************************************************************************/
uint8_t *get_tp_config(int moudle_id,int ic_number)
{
    if(ic_number == 2202)
    {
        switch(moudle_id)
        {
            case OFLIM:
                return G520_2202_OFILM_Config;
            case ECW:
                return G520_2202_ECW_Config;
            case TRULY:
                return G520_2202__TRULY_Config;
            case JUNDA:
                return G520_2202__JUNDA_Config;
            default:
                return NULL;
        }
    }
    else
    {
        return NULL;
    }
}

